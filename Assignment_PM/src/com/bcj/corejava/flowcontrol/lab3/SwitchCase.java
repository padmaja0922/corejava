/**
 * This class is written to display the range of marks based on the grade.
 */
package com.bcj.corejava.flowcontrol.lab3;

import java.util.Scanner;

/**
 * @author Boot Camp User 008
 *
 */
public class SwitchCase {

	/**
	 * This method displays range of marks for  given grade 
	 * 
	 * @param args
	 */
	public static void main(String[] args) {
		
		char c ='F';
		switch (c) {
		case  'A' : System.out.println("80 - 100 "); break;
		case  'B' : System.out.println("73 - 79 "); break;
		case  'C' : System.out.println("65 - 72 "); break;
		case  'D' : System.out.println("55 - 64 "); break;
		case  'E' : System.out.println("<54"); break;
		default : System.out.println(" Not in given grade ");
		 
		}
		
		
	}

}
