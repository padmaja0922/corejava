public class Asterix {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
	 
		for(int i=1;i<=8;i++)
		{
			if(i%2==0)
			System.out.print(" ");
		for(int j=0;j<8;j++)	
			System.out.print( "* " );
	     System.out.println();
		}

	}

}
