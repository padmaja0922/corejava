public class FindLargestNumber {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		int count =0, max =0,x;
		
		Scanner scan = new Scanner(System.in);
		
		for(int i = 0 ;i<5;i++)
		{
		  System.out.println("Enter the number ");
		  x= scan.nextInt();
		  count++;
		  if(max < x)
			  max=x;
		  System.out.println(" the largest number entered so far is "+ max );
		  System.out.println("number of elements entered so far "+count);
		  System.out.println("most recent entered number is "+x);
		}
		
		scan.close();	
			
			
			
			
			
			
			
			
			
			
			
		
		

	}

}
