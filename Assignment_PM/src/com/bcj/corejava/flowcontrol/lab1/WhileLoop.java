/**
 * @author Boot Camp User 008
 *
 *   This class  is written to display all the even numbers 
 *      between 50 and 80 (both inclusive) using While loop.
 */
public class WhileLoop {

	/**
	 * @param args i
	 */
	public static void main(String[] args) {
		
		int i =50; 
	// loop for finding even numbers from 50 and 80 both inclusive
		while( i <=80){
			if(i%2==0)
				 System.out.println(i);
			i++;
		}

	}

}
