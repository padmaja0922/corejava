package com.bcj.corejava.introduction.lab3;

/* Demo to eliminate duplicate using methods*/

public class DemoEliminateDuplicate {

	
// printing first 2 lines of go team	
	public void printsGoTeam() {
		System.out.println("Go, team, go!");
		System.out.println(" You can do it.");
	}

// printing next 2 lines 
	
	public void printTheBest() {

		System.out.println("You're the best,");
		System.out.println(" In the West.");
	}

	public static void main(String[] args) {

		DemoEliminateDuplicate d = new DemoEliminateDuplicate();
		d.printsGoTeam();
		d.printTheBest();
		d.printsGoTeam();
	}

}
