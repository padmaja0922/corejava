public class CharSequenceInterface {
	
 public static String reverseCharSequence(char[] ch){
		 
		 String reverse="";		
		 for(int i =ch.length-1;i>=0;i--)
					reverse=reverse+ch[i];	
		
		 return reverse;		 
	 }
	 
	 public static void main(String[] args) {
		
		 char[] ch={'j','a','v','a'};  
		 System.out.println(reverseCharSequence(ch));
	}

}
